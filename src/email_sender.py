# ── HTML template ─────────────────────────────────────────────────────────────

def _section(title: str, emoji: str, items: list[str], color: str) -> str:
    """Render one analysis section as an HTML block."""
    rows = "".join(
        f"""
        <tr>
          <td style="padding:8px 12px;border-bottom:1px solid #f0f0f0;
                     font-size:14px;color:#333;line-height:1.6;">
            <span style="color:{color};font-weight:600;">▸</span>&nbsp;{item}
          </td>
        </tr>"""
        for item in items
    )
    return f"""
    <tr>
      <td style="padding:20px 0 8px 0;">
        <table width="100%" cellpadding="0" cellspacing="0" border="0"
               style="border-radius:8px;overflow:hidden;
                      border:1px solid #e5e5e5;">
          <tr>
            <td style="background:{color};padding:10px 16px;">
              <span style="color:#fff;font-size:15px;font-weight:700;">
                {emoji}&nbsp;&nbsp;{title}
              </span>
            </td>
          </tr>
          {rows}
        </table>
      </td>
    </tr>"""


