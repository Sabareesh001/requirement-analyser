# ── Sender ────────────────────────────────────────────────────────────────────

def send_email(subject: str, html_body: str) -> None:
    """
    Send *html_body* via Gmail SMTP using credentials from the environment.

    Required env vars:
        GMAIL_SENDER       – your Gmail address
        GMAIL_APP_PASSWORD – Gmail App Password (16-char, no spaces needed)
        RECIPIENT_EMAIL    – destination address
    """
    sender   = os.getenv("GMAIL_SENDER")
    password = os.getenv("GMAIL_APP_PASSWORD")
    recipient = os.getenv("RECIPIENT_EMAIL")

    _require_env("GMAIL_SENDER",       sender)
    _require_env("GMAIL_APP_PASSWORD", password)
    _require_env("RECIPIENT_EMAIL",    recipient)

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = sender
    msg["To"]      = recipient

    msg.attach(MIMEText(html_body, "html", "utf-8"))

    print(f"📧  Connecting to Gmail SMTP (smtp.gmail.com:587) …")
    with smtplib.SMTP("smtp.gmail.com", 587) as server:
        server.ehlo()
        server.starttls()
        server.login(sender, password.replace(" ", ""))
        server.sendmail(sender, recipient, msg.as_string())

    print(f"✅  Email sent successfully → {recipient}")


def _require_env(name: str, value) -> None:
    if not value:
        raise EnvironmentError(
            f"Environment variable '{name}' is not set. "
            "Please add it to your .env file."
        )